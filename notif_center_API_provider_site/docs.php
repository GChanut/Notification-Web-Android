		<br />Notif'Center est un application mobile et une API (Application Programming Interface) Web permettant de récupérer des notifications de l'utilisateur qui se sera préalablement inscrit grâce à ce service. Il pourra donc récupérer ses mails, alertes Facebook, Tweets, et un certains nombre d'informations sur des applications externes utilisant Notif'Center.
		<br />
        <br /> Nous utilisons le tout nouveau protocole de partage de données sécurisé OAuth 1.0a utilisé par les grands Google, Yahoo, Facebook, Twitter,...
		<br />
        <br />Pourquoi ce projet ? Le gros problème des smartphones est biensur la durée de vie de la batterie. Une grosse partie de la ressource est utilisée par de multiples applications tournant en tâche de fond. Ainsi nous utilisons des applications telles que Gmail, Email, Facebook, Twitter restant en synchronisation continue et guettant toute arrivée de notifications sur notre mobile. Notif'Center permet donc grâce à un seul service (plutôt que 3 ou +) de récupérer les notifications des applications Web concernées.
